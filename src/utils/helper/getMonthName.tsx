export const getMonth = (date: string) => {
  const monthName = [
    "Января",
    "Февраля",
    "Марта",
    "Апреля",
    "Мая",
    "Июня",
    "Июля",
    "Августа",
    "Сентября",
    "Октября",
    "Ноября",
    "Декабря",
  ];

  const monthNumber = new Date(date).getMonth();

  return monthName[monthNumber];
};
